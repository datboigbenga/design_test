  <h2 style="text-transform:uppercase">The Need for Grants</h2>

  <p>It’s indeed very difficult it is for some people to finance their education, or the education of their children. Hence,
     various scholarships and student grants are available that offer free money
     that do not have to be repaid. Many college grant programs help students with
     the cost of attending a college. These include both government grants and funding from
     private or institutional donors. One of the most important sources of money for those
     seeking financial assistance to attend college is Pell Grants. The great thing about
     this program is that it’s available for every single one of you out there!</p>

     <p style="text-transform:uppercase">What are Pell Grants?</p>

     <p>Pell Grants are a Federal Program that provides grants to low-income students who
       wish to attend an undergraduate program and certain graduate programs.
       The main differentiator between other college grants and the Pell Grants is
       that Pell Grants can be won by anyone, yes anyone who wishes to attend any of the 5000+ secondary
        institutions and colleges that have signed up for the same.</p>

<p style="text-transform:uppercase">Applying for Pell Grants</p>

<p>When you’re applying for any grant or funding program, you have to understand that it’s going
  to be very competitive; many people are vying for these opportunities and the competition is fierce.
  Hence, you need all the help you can get. This is where we come in. We bring decades of
grant writing experience to you and by working with you, we collaborate with you on your college grant
search by helping you to access the Pell Grant Program.</p>

<p>The amount a person can receive from Pell Grants depend on the following four factors:</p>
<p>
<ul style="padding:0 30px 0">
  <li>The student’s expected family contribution, or EFC</li>
  <li>The cost of attending the particular institution</li>
  <li>The student’s enrollment status as a full or part-time student</li>
  <li>Whether or not the student attends for a full semester.</li>
</ul>
</p>

<p>Calculating Your EFC</p>

<p>Finances play a huge role whenever you apply for a grant. The US government has a specific program in place to calculate your EFC. The most important elements that play a huge role in deciding whether you get a grant or not are:</p>

<p>
  <ul style="padding:0 30px 0">
<li>The Student’s Income</li>
<li>Income of the student’s parents &amp; their combined assets</li>
<li>Size of the family</li>
<li>Number of family members attending college</li>
</ul>
</p>
