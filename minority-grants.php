    <section class="main_con2">
<div class="container">
  <div class="grid">
<div class="con_inner">

  <p>The United States’ government’s commitment towards minorities extends in funding of their businesses and new ventures.
    The government sets aside more than $1 billion on funding projects undertaken by minorities.
    Among these, small businesses started by minorities are important from the government’s perspective because
    they offer a chance for people to start something of their own, gain their own footing in the world of business
    and also generate employment at the local level. As a result, these grants are available at the local,
    State and national level. From a political perspective, these are incredibly important.
    Politicians leverage their position and use it to provide benefits to minorities in exchange for votes.</p>

     <p style="text-transform: uppercase;">Grants for Minority Owned Businesses:</p>

  <p>The U.S. Small Business Administration’s 8(a) Business Development Program helps minority owned businesses
    to grow and develop their business through training workshops, counseling and management and technical guidance.
    They help in administering small business grants for minorities.</p>



  <p>Certain minority groups are presumed to be socially and economically disadvantaged and can qualify for this program.
     Individuals who do not belong to these minority groups are also eligible provided the can
     prove that they have been discriminated against and are economically disadvantaged.</p>

<p style="text-transform: uppercase;">Who is Eligible?:</p>

<p>Minority as a term is not restricted to any particular race or community. Sure, it encompasses
  ethnic groups such as African-American, Hispanic, American Indians, etc. Also, people with cerebral palsy or
  other such illnesses,
  adult males with low income or members of the LGBT community are also eligible for minority grants.</p>

  <p>There are also minority grants available
    for start-up costs in the form of federal minority grants, if you meet the eligibility requirements.</p>

<p style="text-transform: uppercase;">WOMEN</p>

<p>A commonly overlooked fact is that American women from all ethnicities are considered as a minority.
  Women as well as minority businesses can take advantage of grants offered by private institutions and government agencies.
  There are several agencies, both on the national and local level that administer grants to women entrepreneurs.
  There are over 5,000 government sources of grants over $20 billion in grants for starting a small business.</p>


<p>The facts are that more people are taking advantage of minority grants than ever before. The Federal government has come to realize the importance of assisting minority groups
   that have historically been disadvantaged and overlooked. The assistance comes in many forms.</p>


