<h2>First Time Home Buyer Grants </h2>
  <p>If you were to close your eyes and made a wish during these uncertain economic times,
    you might wish for some assurances in your life that you’ll always be able to provide your family
    with the necessities – food, clothing and shelter. You already provide them with love and nurturing,
    and a promise of a better life.
     You dream of your own home with a yard in which the children can play and grow up safely. </p>

     <p>Today, these wishes are no longer pipe dreams.
        There are government grants for first time home buyers that are sure to make a
         difference in this country. The U.S. Department of Housing and Urban Development has first
          time home buyer grants available in every state in the country to put this country
       back on its feet again by helping families to own their own home!</p>

    <p>There is no better time to take advantage of the housing and apartment market than right now. The prices in some areas have gone down 75%, and there are a record number of foreclosed homes available for which government grants are applicable. When the Federal Government offers government grants for first time home buyers, it means that these appropriations are available to those households that have never owned a home previously or have not owned a home within the last three years from the date of the grant application.</p>

    <p>These housing grants apply to down payment and percentage of purchasing price of the home, closing costs, and educational courses designed to make you a more knowledgeable home owner. You can get great deals on condos, townhouses, and on houses in the city, suburbia and in rural areas. In many cases, there is an absolute minimum to put down. Additionally, there are first time home buyer grant opportunities for renovations on foreclosed homes.</p>

    <p>Government Grants.us simplifies the grant application process for you. You still need to select the government grants that best suit your needs; however, the stress and frustration involved in researching these opportunities and accessing the applications is eliminated. Government Grants.us is your one-stop resource center for helping yourself to one of the government grants for first time home buyers.</p>

