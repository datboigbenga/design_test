<div class="con_inner">
<h2 style="text-transform: uppercase;">Section 504 Home Repair Program</h2>
  <p>Offered by the US Department of Agriculture, the intent of this Single Family
    Housing Repair & Loan Program is to help families remain in their homes and keep them
    in a good state of repair, which in turn strengthens communities.
    There are two primary thrusts of this program, the first being to assist those families with very
    low income to repair or improve their homes. The other major demographic served by this program is elderly homeowners,
     who may need to remove hazards or obstacles in the home, or to install safety features that improve mobility and
      accessibility.</p>

<h3>Eligibility for the program is determined according to the following criteria:</h3>


  <p>For grants to the elderly – you must be at least 62, and unable to repay any other kind of loan Income must
     be less than 50% of the average income
    prevalent in your area of residence You must be unable to obtain credit through any other means
    You must own the home you reside in, and which you intend to repair/improve The maximum grant you can
    receive through this program is $7,500 and the maximum loan amount is $20,000, but it is possible to be
     eligible for both, and receive a combined total of $27,500. Loans must be repaid over a period of 20 years,
     at a fixed rate of 1% interest. Grants must be repaid if the owner moves away from the improved home within
     three years of receiving the grant. </p>

<h3>Miscellaneous assistance programs</h3>

  <p>Besides government programs, there is a wide range of other assistance programs that might
     be a fit for your circumstances,
     some of which are local to your area, and some of which are nationwide offerings.</p>
<p>
     <ul style="padding-left:30px;padding-right:30px">

    <li>
    HUD – the Department of Housing and Urban Development awards money for home
    repair grants every year to state agencies. To check your eligibility, search your
    city or county along with the keyword ‘Area Agency on Aging’.</li><br>
    <li>
    Non-profit organizations – a group called ‘Rebuilding Together’ offers home improvement help
     in some communities. Another non-profit housing program called NeighborWorks was created by
     Congress to assist homeowners with financing for home improvements.
     Repairs by Habitat for Humanity helps low-income families for free, to upgrade homes and improve living quality.</li><br>
     <li>
    Local FHA – contact this agency about government home repair grants and home improvement grants.</li><br>
    <li>
    Public utilities – some utility companies will
    award grants to the under-privileged for improving efficiency of appliances, etc.
    Community development organizations – while not available in all locations, yours may
     be an area which does have have a community development organization, and is willing to help.</li><br>
     <li>
    HELOC – a home equity line of credit might be
     obtainable if you have equity in your home, and have some means of repaying it. </li><br>
     <li>
     Home Repair Programs – many cities and counties in the country offer
     housing programs and home repair grants, using block grants given them by the federal government.</li><br>
</ul>
</p>
<p>The bottom line on all these home repair assistance programs and home improvement grants is that there are far more of them than you might think, and if you or someone you know is in a low-income category, you just might be eligible for one or more of them. Do some searching online, or have someone help
   you contact local agencies to find out about the possibilities. Help may be just a phone call away!</p>

<p style="margin-bottom:20px"><a href="apply">Signup</a> now to get access to our database of over 15,000 funding applications and start applying today!</p>

