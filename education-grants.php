        <h1>Education Grants</h1>
      </div>
    </section>
    <section class="main_con2">
<div class="container">
  <div class="grid">
<div class="con_inner">

  <p>Grant money for college is available to nearly any type of student who wishes to pursue a higher education,
     from both public and private sources. We maintain a database of tens of thousands of educational grants for
     college and can assist you in finding and applying for grants that cater to students like you. First though, read on to learn the basics of educational grants and how easy it is for nearly anyone
    headed to college to be matched with grant money for college expenses. </p>

    <h3>What is an Educational Grant?</h3>

     <p>An educational grant is free money from either a public or private source that can be used for college expenses which does not need to be repaid. Numerous educational grants totaling in the billions of dollars are awarded each year to a wide range of individuals from nearly every conceivable background and circumstance. Some educational grant programs have income restrictions, such as the well-known Pell Grant, but many grants are awarded irrespective of income.</p>

<h3>Eligibility for Educational Grants</h3>
    <p>Any United States citizen or eligible non-citizen who has completed high school can apply for an educational grant. The Pell Grant, and all other federal grants, require that students demonstrate financial need. Most state grants are needs-based as well, but many exist that are not. Private corporations, non-profits, and other organizations welcome applicants from an array of student profiles, often regardless of need.</p>

    <p>Further eligibility requirements for federal and state grants include the following:</p>
<p><ul style="padding-left:30px;padding-right:30px">
     <li>Citizen students must possess a valid social security number.</li>
     <li>The student must be enrolled at least half-time.</li>
      <li>Recipients must maintain acceptable grades.</li>
      <li>Applicants and recipients must not be incarcerated.</li>
       <li>Applicants must not be in default on any loans.</li>
     <li>Male citizens must be registered with the Selective Service.</li>
</ul>
</p>


    <p>Non-citizens may wish to visit StudentAid.gov/noncitizen to see a list of non-citizens statuses that are eligible for federal educational grants.</p>

<h3>How Educational Grants Work</h3>

<p>Once you are awarded a grant, the grant award funds will be sent directly to your school. The monies are applied to your student account to pay for educational expenses such as tuition, books, fees, campus housing, and meals. Some schools and grant programs allow for the issuing of refunds to students each year for grant award money that exceeded the amount needed for educational expenses.</p>

<p>Federal government educational grants are funded by the United States taxpayers. State educational grants are funded through lotteries, taxes, or both. Corporate and other private grants are usually funded through donations.</p>

<h3>Government Educational Grants</h3>

<p>The most well-known federal educational grant is the Pell Grant, and those eligible for the Pell grant are also likely to be eligible for educational grants in their home states. Other Federal Government grants include the National Science and Mathematics Access to Retain Talent Grant (SMART), the Federal Supplemental Educational Opportunity Grant (FSEOG), and the Academic Competitiveness Grant (ACG). Federal grants are also offered for African Americans, Latinos, women, veterans, and people with disabilities.</p>

<p>If you do not qualify for federal grants for college, you still may qualify for state educational grants. State grants are normally reserved for residents attending in-state schools; however, many states offer assistance through cooperative programs with other states, especially for specific courses of study. Contact us or your state grant agency for further details, or visit the U.S. Department of Education’s web site for information about educational grants available by state.</p>

<h3>Private Educational Grants</h3>

<p>Private grants for college are also available that cater to certain student criteria, such as gender, race, religious affiliation, veteran and disability status, or course of study. Some private foundations, such as the Gates Millennium Scholars Program, combine their grant programs with federal grant awards programs, providing additional funding to certain minorities who are also eligible for the federal Pell grant.</p>

<p>Companies with household names such as Kelloggs, Coca-Cola, Ford, Dow, and Walmart all offer educational grant opportunities that are outlined on their web site and included in our databases of educational grants. You may also wish to look up smaller organizations that are affiliated with your course of study or interests, such as such as The National Society of Accountants, Institute of Electrical and Electronics Engineers, The American Anthropological Association, and others, or contact us for a complete student profile analysis to match you with grants tailored to your individual background, goals, and interests.</p>

<p>Most private grants are awarded based on a combination of a multiple factors in a student profile. For example, low-income women with at least one child may qualify for up to $5,000 in grant awards from the Patsy Takemoto Mink Education Foundation. Many colleges and universities offer their own college-specific grants, even to students from wealthy families without demonstrated financial need, as part of the school’s competitive strategy to attract and retain certain types of students. When you contact us, we can walk you through your particular school’s grant programs and prepare you for a successful application process.</p>

<h3>Applying for an Educational Grant</h3>

<p>All educational grant programs have their own application process unique to that specific program, as well as a deadline for applying. In most cases, you must already know which school you will be attending, and the institution must meet certain regional or national accreditation standards. Certain schools may also require additional paperwork. Begin by contacting your school’s financial aid department or by contacting a reputable agency specializing in educational grants.</p>

<p>All students seeking federal and state educational grants must first fill out the Free Application for Federal Student Aid (FAFSA), available both through your school’s financial aid office and here, online. Non-citizens may also wish to visit the federal financial aid toolkit for non-citizens for in-depth information on government educational grant opportunities available to them.</p>

<p>Your school’s financial aid office will have applications for private grants available to students of that specific school, but their list will by no means be an exhaustive list of all the educational grants for which you may qualify. College grants awarded by corporations and other private organizations will have their own application instructions that can be obtained through their web site, by calling or writing to them, or by contacting an organization that specializes in matching students with private and public educational grants.</p>

<h3>Conclusion</h3>

<p>If you need free money for college, chances are that an educational grant awards program exists that fits your background and goals. Educational grants for higher education include tuition grants for single parents, grants for women, grants for alumni or children of alumni of certain schools, educational grants for minorities, grants for students from certain geographical areas, grants for veterans and the disabled, grants for students pursuing specific courses of studies, and grants for certain religious or other affiliations, among many others.</p>

